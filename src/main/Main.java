    public int realizarOperacion(int opcion){
        switch (opcion) {
            case 0:
               
                return Integer.parseInt(jTextFieldOp1.getText())*Integer.parseInt(jTextFieldOp2.getText());
                
            case 1:
                
                return Integer.parseInt(jTextFieldOp1.getText())+Integer.parseInt(jTextFieldOp2.getText());
                
            case 2:
                
                return Integer.parseInt(jTextFieldOp1.getText())-Integer.parseInt(jTextFieldOp2.getText());
                
            case 3:
                 return Integer.parseInt(jTextFieldOp1.getText())/Integer.parseInt(jTextFieldOp2.getText());

        }
        return -1000;
    }
   

